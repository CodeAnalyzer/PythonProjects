"""
3.6.1. Максимизация информационного выигрыша: максимальная отдача от усилий

Сравнение трёх различных критериев неоднородности для разбиения деревьев решений:
- Энтропия (критерий информационного выигрыша)
- Неоднородность Джини (критерий индекса Джини)
- Ошибка классификации (критерий ошибки неправильной классификации)

График показывает, что неоднородность Джини является промежуточной мерой между
энтропией и ошибкой классификации.
"""

import matplotlib.pyplot as plt
import numpy as np

def gini(p):
    """
    Вычислить неоднородность Джини
    
    Неоднородность Джини измеряет вероятность неправильной классификации
    случайно выбранного элемента, если он был случайно помечен в соответствии
    с распределением в подмножестве.
    """
    return p * (1 - p) + (1 - p) * (1 - (1 - p))

def entropy(p):
    """
    Вычислить энтропию (информационная мера)
    
    Энтропия измеряет степень неопределённости или неоднородности в узле.
    Более высокая энтропия означает более смешанные классы (большая неоднородность).
    """
    if p == 0 or p == 1:
        return 0
    return -p * np.log2(p) - (1 - p) * np.log2(1 - p)

def error(p):
    """
    Вычислить ошибку классификации
    
    Ошибка классификации измеряет вероятность неправильной классификации
    случайно выбранного элемента из узла.
    """
    return 1 - np.max([p, 1 - p])

# Генерация диапазона вероятностей для класса 1
# p(i=1) представляет вероятность того, что элемент принадлежит классу 1
x = np.arange(0.0, 1.0, 0.01)

# Вычисление индексов неоднородности для каждого значения вероятности
ent = [entropy(p) for p in x]           # Значения энтропии
sc_ent = [e * 0.5 for e in ent]         # Масштабированная энтропия (энтропия/2) для сравнения
gini_vals = [gini(p) for p in x]        # Значения неоднородности Джини
err = [error(i) for i in x]             # Значения ошибки классификации

# Создание графика для визуализации
fig = plt.figure(figsize=(10, 6))
ax = plt.subplot(111)

# Построение каждого критерия неоднородности с разными стилями и цветами
# Каждая линия показывает, как мера неоднородности изменяется с вероятностью класса
for i, lab, ls, c in zip([ent, sc_ent, gini_vals, err],
                        ['Энтропия', 'Энтропия (масштабированная)', 'Неоднородность Джини', 'Ошибка классификации'],
                        ['-', '-', '--', '-.'],  # Стили линий: сплошная, сплошная, пунктирная, штрихпунктирная
                        ['black', 'lightgray', 'red', 'green']):  # Цвета для каждого критерия
    line = ax.plot(x, i, label=lab, linestyle=ls, lw=2, color=c)

# Добавление легенды с форматированием
# Легенда расположена над графиком для лучшей видимости
ax.legend(loc='upper center', bbox_to_anchor=(0.5, 1.15),
          ncol=4, fancybox=True, shadow=False)

# Добавление опорных линий на уровнях y=0.5 и y=1.0 для лучшей интерпретации
# Они помогают визуализировать диапазон значений неоднородности
ax.axhline(y=0.5, linewidth=1, color='k', linestyle='--')
ax.axhline(y=1.0, linewidth=1, color='k', linestyle='--')

# Установка меток осей и пределов
plt.ylim([0, 1.1])  # Ось Y от 0 до 1.1 для отображения полного диапазона
plt.xlabel('p(i=1)')  # Ось X: вероятность принадлежности классу 1
plt.ylabel('Индекс неоднородности')  # Ось Y: значение меры неоднородности
plt.title('Сравнение критериев неоднородности для разбиения деревьев решений')
plt.grid(True, alpha=0.3)  # Добавление лёгкой сетки для лучшей читаемости
plt.show()  # Отображение графика
