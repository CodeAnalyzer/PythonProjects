import pandas as pd
import numpy as np

# Устанавливаем seed для воспроизводимости
np.random.seed(123)

# Определяем переменные и метки
variables = ['X', 'Y', 'Z']
labels = ['ID_0', 'ID_1', 'ID_2', 'ID_3', 'ID_4']

# Генерируем случайные данные (5 наблюдений, 3 признака)
X = np.random.random_sample([5, 3]) * 10

# Создаем DataFrame
df = pd.DataFrame(X, columns=variables, index=labels)

print("=== СЛУЧАЙНАЯ ВЫБОРКА ДАННЫХ ===")
print(f"Размер набора данных: {df.shape}")
print()
print("DataFrame:")
print(df)
print()

# Дополнительная информация о данных
print("=== ИНФОРМАЦИЯ О ДАННЫХ ===")
print(df.describe())
print()

print("Примечание:")
print("- Строки представляют разные наблюдения (ID_0 - ID_4)")
print("- Столбцы представляют различные признаки (X, Y, Z)")
print("- Данные сгенерированы случайно в диапазоне [0, 10]")
